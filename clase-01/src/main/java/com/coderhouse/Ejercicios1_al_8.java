package com.coderhouse;

public class Ejercicios1_al_8 {

	public static void main(String[] args) {
		// Ejercicio1
		// Crear una variable entera que almacene la edad de un usuario.
		int edad = 32;
		System.out.println("1 - Mi edad es: "+edad);

		// Ejercicio2
		// Definir una constante para el número de días en una semana.
		final int diasSemana = 7;
		System.out.println("2 - La cantidad de dias en una semana son: "+diasSemana);
		
		// Ejercicio3
		// Declarar una variable de tipo double para almacenar el precio de un producto.
		double precioProducto = 100.32d;
		System.out.println("3 - El precio del producto es: "+precioProducto);
		
		// Ejercicio4
		// Crear una variable booleana que indique si un estudiante está aprobado o no.
		boolean aprobo = false;
		System.out.println("4 - El estudiante esta aprobado? "+aprobo);
		
		// Ejercicio5
		// Utilizar una variable de tipo char para almacenar la inicial del nombre de una persona.
		char miInicial = 'G';
		System.out.println("5 - La inicial de mi nombre es: "+miInicial);
		
		// Ejercicio6
		// Crear un arreglo de enteros que almacene las notas de 5 estudiantes.
		int[] notas = new int[5];
		notas[0] = 10;
		notas[1] = 9;
		notas[2] = 8;
		notas[3] = 7;
		notas[4] = 6;
		
		System.out.println("6 - NOTAS!... Alumno A: "+notas[0] + " Alumno B: "+notas[1] + " Alumno C: "+notas[2] + " Alumno D: "+notas[3] + " Alumno E: "+notas[4]);
		
		// Ejercicio7
		// Definir una variable de tipo String que almacene el nombre completo de una persona.
		String miNombreCompleto = "Gary David Grageda Cruz";
		System.out.println("7 - Mi nombre completo: "+miNombreCompleto);
		
		// Ejercicio8
		// Crear una variable float para almacenar el peso de una persona.
		float miPeso = 94.5f;
		System.out.println("8 - Mi peso es: "+miPeso);
		
	}

}
